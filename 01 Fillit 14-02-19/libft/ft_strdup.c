#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*tmp;
	int		i;

	if (!(tmp = (char *)malloc(sizeof(*s1) * (ft_strlen(s1) + 1))))
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		tmp[i] = s1[i];
		i++;
	}
	tmp[i] = '\0';
	return (tmp);
}
