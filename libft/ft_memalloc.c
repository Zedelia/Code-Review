#include "libft.h"
#include <stdlib.h>

void	*ft_memalloc(size_t size)
{
	size_t	i;
	char	*thing;

	if (!(thing = (char *)malloc(size)))
		return (NULL);
	i = 0;
	while (i < size)
		thing[i++] = 0;
	return (thing);
}
